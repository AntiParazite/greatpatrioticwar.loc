<h3>Конструкция</h3>
	<p>Карабин состоит из следующих частей и механизмов:</p>
	<ul>
		<li>Ствол со ствольной коробкой, штыком и прицельными приспособлениями,</li>
		<li>крышка ствольной коробки,</li>
		<li>затвор,</li>
		<li>возвратный механизм,</li>
		<li>газовая трубка со ствольной накладкой,</li>
		<li>газовый поршень,</li>
		<li>толкатель с пружиной,</li>
		<li>ударно-спусковой механизм,</li>
		<li>магазин;</li>
		<li>ложа.</li>
	</ul>
	<p>Для быстрого наполнения неотъёмного магазина на 10 патронов используется обойма. Для использования обоймы на затворной раме имеются специальные направляющие. По израсходывании патронов, при пустом магазине, после последнего выстрела затвор становится на затворную задержку, что снижает время на последующее пополнение магазина патронами.</p>
<img class="image_left" src="Images\Weapons\СКС-3.jpg"/>
	<p>Прицельные приспособления СКС состоят из прицела и мушки. Прицел в свою очередь состоит из колодки прицела с пластинчатой пружиной, прицельной планки и хомутика с защёлками и пружинами. Колодка прицела имеет два сектора для придания прицельной планке определенной высоты. На планке нанесена шкала с делениями от 1 до 10 (обозначающими дальность стрельбы в сотнях метров) и буквой «П» (постоянная установка прицела, соответствующая прицелу 3).</p>
	<p>Штык — клинковый неотъёмно-откидной, состоит из клинка с основанием и трубки с пружиной (обеспечивающей амортизацию штыка для однообразного боя СКС независимо от положения штыка). Некоторые ранние карабины имели игольчатый штык.</p>
	<p>В комплект карабина входят: принадлежность (шомпол, протирка, ёршик, выколотка, пенал и маслёнка), ремень, патронные сумки и обоймы.</p>
	<p>Перезаряжание СКС после очередного выстрела производится автоматически, для чего используется энергия пороховых газов, отводимых из канала ствола. Запирание канала ствола осуществляется путём перекоса затвора вниз. Ствол чернокованный, не хромированный.</p>
