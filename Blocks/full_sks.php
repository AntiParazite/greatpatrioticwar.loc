	<p><b>7,62-мм самозарядный карабин Симонова</b> (<b>СКС</b>, за рубежом также известен как <b>СКС-45</b>) — советский самозарядный карабин конструкции Сергея Симонова, принят на вооружение в 1949 году.</p>
	<p>Первые экземпляры начали поступать в действующие части в начале 1945 года — это был единственный случай применения патрона 7,62×39 мм во Второй мировой войне.</p>
<h3>История</h3>
	<p>После принятия в 1943 году на вооружение промежуточного патрона 7,62×39 мм конструкции Н. М. Елизарова и Б. В. Сёмина развернулись работы по созданию новой системы стрелкового вооружения под этот патрон. Первые образцы СКС под патрон 7,62×39 мм были созданы Симоновым к концу 1944 года на основе карабина, разработанного им же в рамках конкурса на новый карабин ещё в 1940—1941 годах, но не пошедшего в производство из-за эвакуации заводов. Помимо патрона отличиями нового карабина стали неотъёмно-откидной штык, отсутствие дульного тормоза-компенсатора, съёмная газовая камора. Небольшая партия новых карабинов проходила испытания в действующих частях 1 Белорусского фронта в начале 1945 года, а также поставлялась на курсы «Выстрел». Оружие получило положительные отзывы, однако доводка карабина и самого нового патрона продолжалась вплоть до 1949 года, когда СКС поступил на вооружение Советской Армии.</p>
