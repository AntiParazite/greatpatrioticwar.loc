	<p>Расчет РККА с ДПСледует отметить, что, хотя внешне магазин ДП и напоминает магазин пулемёта Льюиса, на самом деле он представляет собой соврешенно отличную по принципу действия конструкцию; например, у «Льюиса» патронный диск проворачивается за счёт энергии затвора, передаваемой на него сложной системой рычагов, а у ДП — за счёт предварительно взведённой пружины в самом магазине.</p>
	<p>УСМ пулемёта позволял вести только автоматический огонь с открытого затвора. Он был выполнен в виде съёмного модуля, крепящегося к коробу поперечной чекой. Обычного предохранителя не имелось, вместо него был выполнен автоматический предохранитель в виде клавиши, выключавшийся при охвате рукой шейки приклада. При ведении интенсивного огня необходимость постоянно удерживать клавишу предохранителя нажатой утомляла стрелка, а приклад винтовочного типа не способствовал прочному удержанию оружия при стрельбе очередями. Более удачной оказалась конструкция блока УСМ танкового пулемёта ДТ, который имел обычный предохранитель и пистолетную рукоятку. Модернизированный вариант пулемёта — ДПМ — получил аналогичный ДТ блок УСМ; также неавтоматический предохранитель в дополнение к родному автоматическому вводился в конструкцию финских ДП в процессе их капремонта.</p>
	<p>Огонь из ДП вёлся со съёмных сошек, которые в горячке боя порой терялись из-за неудачного крепления или разбалтывались, что, в свою очередь, существенно ухудшало удобство применения пулемёта. Поэтому на ДПМ ввели несъёмные сошки. Выброс стрелянных гильз осуществлялся вниз.</p>
<h3>Применение</h3>
	<p>Во время учений и боевых действий пулемёт обслуживали два человека: стрелок и его помощник, который переносил короб с 3 дисками. Также при cтрельбе из положения лёжа к пулемёту обеими концами привязывалась длинная лента, и боец, натягивая её ногой, сильнее прижимал приклад к плечу. Таким образом, колебания пулемёта уменьшались и повышалась точность стрельбы.</p>
	<p>Дягтярев пулемет устанавливали на мотоциклы М-72. Конструкция крепления пулемёта на мотоцикле М-72 (реконструкция) к коляске позволяла вести огонь даже по самолётам. Однако, этот способ борьбы с самолётами был не очень удобным: для стрельбы нужно было останавливаться, затем боец вылезал из коляски и из положения «сидя» вёл огонь по воздушным целям.</p>
	<p>После принятия на вооружение пулемёта ДП, состоявшие до этого на вооружении Красной армии английские пулемёты Льюиса образца 1915 г. постепенно уходили на склады, хотя в критические моменты Великой Отечественной «Льисы» вынужденно использовались отдельными воинскими подразделениями, как и многие другие устаревшие образцы вооружения.</p>
