<h3>Пулемет Дягтярева - пехотный пулемет РККА</h3>
	<p><b>ДП (Дегтярёва Пехотный)</b> — ручной пулемёт, разработанный В. А. Дегтярёвым и принятый на вооружение РККА в 1927 году. ДП стал одним из первых образцов стрелкового оружия, созданных в СССР. Пулемёт массово использовался в качестве основного оружия огневой поддержки пехоты звена взвод-рота вплоть до конца Великой Отечественной войны.</p>
	<p>Трофейные образцы ДП использовали в Вермахте (под обозначением «7,62mm leichte Maschinengewehr 120(r)»).Кроме того, за счёт захваченных в Зимней войне 1939-40 годов и позднее трофеев это был один из наиболее массовых образцов ручных пулемётов в финской армии в период Второй мировой войны ввиду, не в последнюю очередь, существенного превосходства над финским пулемётом Лахти-Салоранта.</p>
	<p>По окончании войны пулемёт ДП и его модернизированный вариант ДПМ, созданный по опыту боевых действий в 1943—44 годах, были сняты с вооружения Советской Армии и широко поставлялись дружественным СССР странам. На вооружении государств-участников ОВД состоял до 1960-х годов. Применялся в Корее, Вьетнаме и других странах.</p>
<h3>Конструкция</h3>
	<p>Ручной пулемёт ДП является оружием с автоматикой на основе отвода пороховых газов и магазинным питанием. Газовый двигатель имеет поршень с длинным рабочим ходом и газовый регулятор, расположенные под стволом.</p>
	<p>Сам ствол — быстросменный, частично скрыт защитным кожухом и оснащен коническим съемным пламегасителем. Ствол порой не выдерживал интенсивной стрельбы: поскольку он был тонкостенным, он быстро нагревался (особенно на поздних выпусках, у которых для упрощения ствол был выполнен без ребристого радиатора), и для того, чтобы не вывести пулемёт из строя, приходилось стрелять короткими очередями (боевая скорострельность пулемёта — до 80 выстрелов в минуту). Смена ствола непосредственно во время боя была затруднена: она требовала специального ключа для снятия его замыкателя и защиты рук от ожогов.</p>
	<p>Запирание ствола осуществлялось двумя боевыми упорами, разводимыми в стороны при движении ударника вперед. После того, как затвор приходит в переднее положение, затворная рама продолжает движение, при этом уширенная средняя часть соединённого с ней ударника, воздействуя изнутри на задние части боевых упоров, разводит их в стороны, в пазы ствольной коробки, жёстко запирая затвор. После выстрела затворная рама под действием газового поршня начинает движение назад. При этом ударник отводится назад, а специальные скосы рамы сводят боевые упоры, выводя их из зацепления со ствольной коробкой и отпирая затвор. Возвратная пружина располагалась под стволом и при интенсивном огне перегревалась, теряя упругость, что было одним из сравнительно немногочисленных, но существенных недостатков пулемета ДП. Кроме того, боевые упоры требовали точной подгонки для достижения симметричного запирания (что не представляло на практике существенного недостатка).</p>
	<p>Питание осуществлялось из плоских дисковых магазинов — «тарелок», в которых патроны располагались по окружности, пулями к центру диска. Такая конструкция обеспечивала надёжную подачу патронов с выступающей закраиной, однако имела и существенные недостатки: большие габариты, а также масса пустого магазина, неудобство в транспортировке и заряжании, а также возможность повреждения магазина в условиях боя из-за его склонности к деформации. Емкость магазина изначально составляла 49 патронов, позднее были введены 47-патронные с повышенной надёжностью действия. К пулемёту придавалось три магазина с металлическим коробом для их переноски.</p>
